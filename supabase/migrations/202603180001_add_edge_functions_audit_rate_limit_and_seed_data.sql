create or replace function hospital.consume_rate_limit(
  p_scope text,
  p_subject text,
  p_max_requests integer,
  p_window_seconds integer default 60
)
returns table (
  allowed boolean,
  requests_used integer,
  reset_at timestamptz
)
language plpgsql
security definer
set search_path = hospital, public
as $$
declare
  v_now timestamptz := now();
  v_window_start timestamptz;
  v_count integer;
begin
  if p_scope is null or p_subject is null or p_max_requests is null or p_window_seconds is null then
    raise exception 'invalid_rate_limit_input';
  end if;

  v_window_start := to_timestamp(floor(extract(epoch from v_now) / p_window_seconds) * p_window_seconds);

  insert into hospital.edge_request_counters as rc (scope, subject, window_start, request_count)
  values (p_scope, p_subject, v_window_start, 1)
  on conflict (scope, subject, window_start)
  do update set request_count = rc.request_count + 1
  returning rc.request_count into v_count;

  allowed := v_count <= p_max_requests;
  requests_used := v_count;
  reset_at := v_window_start + make_interval(secs => p_window_seconds);

  return next;
end;
$$;

revoke all on function hospital.consume_rate_limit(text, text, integer, integer) from public;
grant execute on function hospital.consume_rate_limit(text, text, integer, integer) to anon, authenticated, service_role;

