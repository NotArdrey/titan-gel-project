create table if not exists hospital.edge_request_counters (
  id bigint generated by default as identity primary key,
  scope text not null,
  subject text not null,
  window_start timestamptz not null,
  request_count integer not null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (scope, subject, window_start)
);

create table if not exists hospital.edge_audit_logs (
  id bigint generated by default as identity primary key,
  function_name text not null,
  endpoint text not null,
  action text not null,
  actor_user_id uuid,
  actor_role text,
  request_ip text,
  request_id text,
  status_code integer,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);

create table if not exists hospital.edge_error_logs (
  id bigint generated by default as identity primary key,
  function_name text not null,
  endpoint text not null,
  actor_user_id uuid,
  request_ip text,
  request_id text,
  error_message text not null,
  error_stack text,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);

create index if not exists idx_edge_request_counters_scope_subject_window on hospital.edge_request_counters (scope, subject, window_start desc);
create index if not exists idx_edge_audit_logs_created_at on hospital.edge_audit_logs (created_at desc);
create index if not exists idx_edge_audit_logs_function_endpoint on hospital.edge_audit_logs (function_name, endpoint);
create index if not exists idx_edge_error_logs_created_at on hospital.edge_error_logs (created_at desc);
create index if not exists idx_edge_error_logs_function_endpoint on hospital.edge_error_logs (function_name, endpoint);

drop trigger if exists trg_edge_request_counters_updated_at on hospital.edge_request_counters;
create trigger trg_edge_request_counters_updated_at
before update on hospital.edge_request_counters
for each row execute function hospital.set_updated_at();

